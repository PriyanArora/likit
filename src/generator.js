function generatedConfig(options, cwd) {
  const teamMode = Boolean(options.teamMode);
  const username = teamMode ? (options.username || gitUsername(cwd) || 'developer') : null;
  return {
    version: VERSION,
    created: new Date().toISOString(),
    mode: options.mode,
    tool: options.tool,
    projectType: options.projectType,
    teamMode,
    username: username ? slugify(username) : null
  };
}

function dryRunFileList(options, cwd) {
  assertOptions(options);
  const source = templateDir(options);
  const files = listTemplateFiles(source).map((file) => destinationFor(options.tool, file));
  files.push(path.join('.likit', 'config.json'));
  files.push(path.join('.likit', 'HOW_TO_USE.md'));
  if (options.teamMode) {
    const username = slugify(options.username || gitUsername(cwd) || 'developer');
    files.push(path.join('.likit', `Progress_${username}.md`));
  }
  const gitignore = path.join(cwd, '.gitignore');
  if (!fs.existsSync(gitignore)) {
    files.push('.gitignore');
  } else {
    const content = fs.readFileSync(gitignore, 'utf8');
    const hasEnv = content
      .split(/\r?\n/)
      .map((line) => line.trim())
      .some((line) => line === '.env');
    if (!hasEnv) files.push('.gitignore');
  }
  return Array.from(new Set(files)).sort();
}

function generateFiles(options) {
  const cwd = options.cwd || process.cwd();
  assertOptions(options);

  const source = templateDir(options);
  const templateFiles = listTemplateFiles(source);
  const generatedFiles = [];

  if (options.dryRun) {
    console.log('[likit] Dry run file list:');
    for (const file of dryRunFileList(options, cwd)) {
      console.log(`- ${file}`);
    }
    console.log('[DRY RUN] No files written.');
    return { generatedFiles: dryRunFileList(options, cwd) };
  }

  for (const relativeFile of templateFiles) {
    const destRelative = destinationFor(options.tool, relativeFile);
    if (
      options.preserveUserContent
      && PROTECTED_USER_FILES.has(path.basename(destRelative))
      && fs.existsSync(path.join(cwd, destRelative))
    ) {
      continue;
    }
    const sourceFile = path.join(source, relativeFile);
    const destFile = path.join(cwd, destRelative);
    ensureDir(path.dirname(destFile));
    fs.copyFileSync(sourceFile, destFile);
    generatedFiles.push(destRelative);
    console.log(`\u2713 ${destRelative}`);
  }

  const likitDir = path.join(cwd, '.likit');
  ensureDir(likitDir);
  const config = generatedConfig(options, cwd);
  writeJson(path.join(likitDir, 'config.json'), config);
  generatedFiles.push(path.join('.likit', 'config.json'));
  console.log('\u2713 .likit/config.json');

  fs.writeFileSync(path.join(likitDir, 'HOW_TO_USE.md'), howToUse(config));
  generatedFiles.push(path.join('.likit', 'HOW_TO_USE.md'));
  console.log('\u2713 .likit/HOW_TO_USE.md');

  if (config.teamMode) {
    const progressTemplate = path.join(source, 'Progress.md');
    const progressContent = fs.existsSync(progressTemplate)
      ? fs.readFileSync(progressTemplate, 'utf8')
      : '# Progress\n\n**Current Gate:** G0\n';
    const personalProgress = path.join(likitDir, `Progress_${config.username}.md`);
    fs.writeFileSync(personalProgress, `${progressContent.trimEnd()}${progressFooter(config)}`);
    generatedFiles.push(path.join('.likit', `Progress_${config.username}.md`));
    console.log(`\u2713 .likit/Progress_${config.username}.md`);

    const rootProgress = [
      '# Progress',
      '',
      'Team mode - see `.likit/` for per-developer progress.',
      '',
      `Current shared setup: ${MODES[config.mode]} \u00b7 ${TOOLS[config.tool]} \u00b7 ${config.projectType}`,
      progressFooter(config).trimEnd(),
      ''
    ].join('\n');
    fs.writeFileSync(path.join(cwd, 'Progress.md'), rootProgress);
  } else {
    appendProgressFooter(path.join(cwd, 'Progress.md'), config);
  }

  writeGitignore(cwd, false, generatedFiles);

  console.log(TOOL_START[config.tool]);
  return { config, generatedFiles };
}

