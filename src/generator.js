const PROTECTED_USER_FILES = new Set(['Progress.md', 'ProjectSummary.md']);

function ensureDir(dir) {
  fs.mkdirSync(dir, { recursive: true });
}

function readJson(file) {
  return JSON.parse(fs.readFileSync(file, 'utf8'));
}

function writeJson(file, value) {
  fs.writeFileSync(file, `${JSON.stringify(value, null, 2)}\n`);
}

function readConfig(cwd) {
  const configPath = path.join(cwd, '.likit', 'config.json');
  if (!fs.existsSync(configPath)) {
    throw new Error('No .likit/config.json found. Run: npx likit init');
  }
  return readJson(configPath);
}

function tryReadConfig(cwd) {
  try {
    return readConfig(cwd);
  } catch (_error) {
    return null;
  }
}

function templateRoot() {
  return path.resolve(__dirname, '..', 'templates');
}

function templateDir(options) {
  return path.join(templateRoot(), options.mode, options.tool);
}

function isValidChoice(value, map) {
  return Object.prototype.hasOwnProperty.call(map, value);
}

function assertOptions(options) {
  if (!isValidChoice(options.mode, MODES)) {
    throw new Error(`Invalid mode: ${options.mode}`);
  }
  if (!isValidChoice(options.tool, TOOLS)) {
    throw new Error(`Invalid tool: ${options.tool}`);
  }
  if (!isValidChoice(options.projectType, PROJECT_TYPES)) {
    throw new Error(`Invalid project type: ${options.projectType}`);
  }
}

function listTemplateFiles(dir) {
  if (!fs.existsSync(dir)) {
    throw new Error(`Template directory missing: ${dir}`);
  }

  const results = [];
  const stack = [dir];
  while (stack.length > 0) {
    const current = stack.pop();
    for (const entry of fs.readdirSync(current, { withFileTypes: true })) {
      const full = path.join(current, entry.name);
      if (entry.isDirectory()) {
        stack.push(full);
      } else if (entry.isFile()) {
        results.push(path.relative(dir, full));
      }
    }
  }
  return results.sort();
}

function destinationFor(tool, relativeFile) {
  const file = relativeFile.replaceAll(path.sep, '/');
  if (tool === 'cursor' && file === 'likit.mdc') {
    return path.join('.cursor', 'rules', 'likit.mdc');
  }
  if (tool === 'windsurf' && file === 'likit.windsurfrules') {
    return '.windsurfrules';
  }
  return file;
}

function slugify(value) {
  return String(value || 'developer')
    .trim()
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '') || 'developer';
}

function gitUsername(cwd) {
  try {
    const output = childProcess.execSync('git config user.name', {
      cwd,
      encoding: 'utf8',
      stdio: ['ignore', 'pipe', 'ignore']
    }).trim();
    return output ? slugify(output) : null;
  } catch (_error) {
    return null;
  }
}

function progressFooter(options) {
  return [
    '',
    '---',
    `Generated by Likit v${VERSION} \u00b7 Mode: ${MODES[options.mode]} \u00b7 Tool: ${TOOLS[options.tool]} \u00b7 Type: ${options.projectType}`,
    ''
  ].join('\n');
}

function appendProgressFooter(file, options) {
  if (!fs.existsSync(file)) return;
  const content = fs.readFileSync(file, 'utf8');
  if (content.includes('Generated by Likit v')) return;
  fs.writeFileSync(file, `${content.trimEnd()}${progressFooter(options)}`);
}

function writeGitignore(cwd, dryRun, generatedFiles) {
  const file = path.join(cwd, '.gitignore');
  const relative = '.gitignore';
  let content = '';

  if (fs.existsSync(file)) {
    content = fs.readFileSync(file, 'utf8');
  }

  const hasEnv = content
    .split(/\r?\n/)
    .map((line) => line.trim())
    .some((line) => line === '.env');

  if (hasEnv) return;

  generatedFiles.push(relative);
  if (dryRun) return;

  const addition = '# Likit: secrets should never be in the repo (H8)\n.env\n';
  fs.writeFileSync(file, content.trim() ? `${content.trimEnd()}\n\n${addition}` : addition);
}

