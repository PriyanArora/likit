function printVersion({ cwd }) {
  const config = tryReadConfig(cwd);
  console.log(`likit ${VERSION}`);
  if (config) {
    console.log(`mode=${config.mode} tool=${config.tool} projectType=${config.projectType} teamMode=${config.teamMode}`);
  } else {
    console.log('No .likit/config.json found.');
  }
}

function teamStatus({ cwd }) {
  const config = readConfig(cwd);
  if (!config.teamMode) {
    console.log('[likit] Team mode is not enabled for this project.');
    return;
  }

  const likitDir = path.join(cwd, '.likit');
  const files = fs.existsSync(likitDir)
    ? fs.readdirSync(likitDir).filter((file) => /^Progress_.+\.md$/.test(file))
    : [];

  if (files.length === 0) {
    console.log('[likit] No per-developer progress files found.');
    return;
  }

  console.log('Developer\tCurrent Gate\tCompleted Items');
  for (const file of files.sort()) {
    const content = fs.readFileSync(path.join(likitDir, file), 'utf8');
    const gateMatch = content.match(/\*\*Current Gate:\*\*\s*(.+)/);
    const done = (content.match(/- \[x\]/g) || []).length;
    const name = file.replace(/^Progress_/, '').replace(/\.md$/, '');
    console.log(`${name}\t${gateMatch ? gateMatch[1].trim() : 'unknown'}\t${done}`);
  }
}

